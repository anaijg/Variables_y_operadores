package operadores.aritmeticos;

public class EjemplosAritmeticos {
    public static void main(String[] args) {
        // suma
        int a = 13;
        int b = 5;
        System.out.println("Suma: " + (a + b));
        // resta
        System.out.println("Resta: " + (a - b));
        // multiplicación
        System.out.println("Multiplicación: " + (a * b));
        // cociente
        System.out.println("Cociente de división entera: " + (a / b));
        // resto (módulo)
        System.out.println("Resto (módulo) de división entera: " + (a % b));

    }
}
